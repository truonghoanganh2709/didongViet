const makeImage = (label, color = '#d70018') => {
  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" width="520" height="520" viewBox="0 0 520 520">
      <rect width="520" height="520" rx="48" fill="#f8fafc"/>
      <rect x="154" y="66" width="212" height="388" rx="34" fill="${color}"/>
      <rect x="174" y="100" width="172" height="296" rx="18" fill="#ffffff"/>
      <circle cx="260" cy="425" r="15" fill="#ffffff"/>
      <text x="260" y="254" text-anchor="middle" font-family="Arial, sans-serif" font-size="34" font-weight="700" fill="${color}">${label}</text>
    </svg>
  `

  return `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`
}

